export function getEthicsContext(): string {
  return `ETHICS: CORE POSITIONS AND ARGUMENTS

=== NORMATIVE ETHICS ===

UTILITARIANISM (Consequentialist)
Thesis: An action is right iff it maximizes overall utility (well-being, preference satisfaction).
Key advocates: Bentham, Mill, Singer
Key claims: Consequences determine rightness; well-being is the only intrinsic good; impartially maximize utility
Strengths: Clear decision procedure; respects what matters; applicable to policy
Weaknesses: Demandingness; fails to respect individual rights; measurement problems

DEONTOLOGICAL ETHICS (Rights-based)
Thesis: Rightness depends on conforming to duties/rules, not just consequences.
Key advocates: Kant, Ross, Regan
Key claims: Some acts inherently wrong regardless of consequences; rights constrain utility maximization; persons have intrinsic dignity
Strengths: Explains why we shouldn't use people as means; protects individual rights
Weaknesses: Determining which duties bind; conflicts between duties; unintuitive implications

VIRTUE ETHICS (Character-based)
Thesis: Ethics is fundamentally about developing good character and living well (eudaimonia).
Key advocates: Aristotle, Foot, Hursthouse
Key claims: Moral education cultivates virtues; virtuous person has practical wisdom; flourishing is the goal
Strengths: Explains moral development; emphasizes context-sensitivity; holistic view
Weaknesses: Doesn't clearly specify what makes an action right; cultural differences

CARE ETHICS
Thesis: Morality is grounded in relationships, interdependence, and responsiveness to others' needs.
Key advocates: Noddings, Held, Kittay
Key claims: Universal principles miss what matters; dependence fundamental; moral agency maintains relationships
Strengths: Explains motivation; highlights relational nature; effective for real situations
Weaknesses: Seems parochial; unclear application beyond intimate relationships

=== META-ETHICS ===

MORAL REALISM: Moral facts exist objectively, independent of what anyone believes.
MORAL RELATIVISM: Moral judgments are true or false relative to a culture or individual.

=== APPLIED ETHICS: KEY CASES ===

THE TROLLEY PROBLEM
Scenario: Trolley hurtling toward five people. Pull lever to divert, killing one instead.
Utilitarian: Pull lever (1 death < 5 deaths)
Deontological: Depends on rule; maybe impermissible to intentionally cause death
Virtue ethics: What would virtuous person do?

INFORMED CONSENT
Medical treatment only with consent from competent, informed patient.
Key principles: Autonomy, Beneficence, Non-maleficence
AI relevance: Medical AI must respect patient autonomy

ALGORITHMIC BIAS IN AI
AI systems trained on biased data perpetuate discrimination.
Tensions: Utilitarian vs rights-based vs virtue vs care ethics approaches

=== KEY CONCEPTS ===

MORAL AGENCY: Capacity to act from moral principle.
MORAL STATUS: Standing to be morally considered; having interests that matter morally.
MORAL PROGRESS: Improvement in moral understanding and practice over time.
DOCTRINE OF DOUBLE EFFECT: Action with both good and bad effects can be permissible if good effect is intended and outweighs bad.`.trim();
}
